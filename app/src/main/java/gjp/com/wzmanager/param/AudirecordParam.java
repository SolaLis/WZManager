package gjp.com.wzmanager.param;

import java.io.Serializable;

/**
 * Created by Administrator on 2017/7/31.
 */

public class AudirecordParam implements Serializable{
    private String usercode;
    private String module;
    private String auditingflag;
    private int currentPage;
    private int pageSize;

    public AudirecordParam() {
    }

    public String getUsercode() {
        return usercode;
    }

    public void setUsercode(String usercode) {
        this.usercode = usercode;
    }

    public String getModule() {
        return module;
    }

    public void setModule(String module) {
        this.module = module;
    }

    public String getAuditingflag() {
        return auditingflag;
    }

    public void setAuditingflag(String auditingflag) {
        this.auditingflag = auditingflag;
    }

    public int getCurrentPage() {
        return currentPage;
    }

    public void setCurrentPage(int currentPage) {
        this.currentPage = currentPage;
    }

    public int getPageSize() {
        return pageSize;
    }

    public void setPageSize(int pageSize) {
        this.pageSize = pageSize;
    }
}
