package gjp.com.wzmanager.bean;

import java.util.List;

/**
 * Created by Administrator on 2017/8/4.
 */

public class ModuleBean {
    private String name;
    private List<MBean> mBeanList;

    public ModuleBean() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<MBean> getmBeanList() {
        return mBeanList;
    }

    public void setmBeanList(List<MBean> mBeanList) {
        this.mBeanList = mBeanList;
    }

    public static class MBean {
        private String module;
        private String modulec;
        public MBean() {
        }

        public MBean(String module, String modulec) {
            this.module = module;
            this.modulec = modulec;
        }

        public String getModule() {
            return module;
        }

        public void setModule(String module) {
            this.module = module;
        }

        public String getModulec() {
            return modulec;
        }

        public void setModulec(String modulec) {
            this.modulec = modulec;
        }
    }
}
